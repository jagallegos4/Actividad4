    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        listaV = ListaVendedor.obtenerInstancia();
        boolean validar = listaV.validarCedula(jTextField1.getText());
        if(validar){
            listaV.guardarVendedor(jTextField1.getText(), jTextField2.getText(), jTextField3.getText(),
                    jComboBox1.getSelectedItem().toString(), jTextField4.getText());
            JOptionPane.showMessageDialog(null, "Vendedor Ingresado Correctamente");
            limpiar();
        }
        else{
            JOptionPane.showMessageDialog(null, "Numero de Cedula Incorrecto");
            ingresoP = new IngresoProveedor();
            ingresoP.show();
            this.dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

